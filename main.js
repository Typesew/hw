var month = prompt('Введите сезон с заглавной буквы)');
switch(month){
    case 'Зима':
        alert('Антарктида');
        break;
    case 'Лето':
        alert('Мальдивы');
        break;
    case 'Весна':
        alert('Испания');
        break;
    case 'Осень':
        alert('Ирландия');
        break;
    default:
        alert();
}
